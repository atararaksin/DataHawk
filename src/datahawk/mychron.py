REHANDSHAKE = "3c685354435008000000003e00000000060800003c535443500e003e"


@dataclass
class Session:
    """A single session on the MyChron 5 device."""
    name: str
    size: int
    date: str
    time: str
    laps: str
    track: str


def _recv(sock, idle=0.3, max_t=5.0):
    data = bytearray()
    deadline = time.time() + max_t
    last = time.time()
    while time.time() < deadline:
        wait = min(idle - (time.time() - last), deadline - time.time())
        if wait <= 0:
            break
        sock.settimeout(max(0.05, wait))
        try:
            chunk = sock.recv(65536)
            if not chunk:
                break
            data.extend(chunk)
            last = time.time()
        except socket.timeout:
            if data and (time.time() - last) >= idle:
                break
    return bytes(data)


def check_device(ip: str = DEVICE_IP) -> bool:
    """Return True if device responds to UDP keepalive."""
    ka = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    ka.settimeout(3.0)
    ka.sendto(b"aim-ka", (ip, UDP_PORT))
    try:
        ka.recvfrom(4096)
        return True
    except socket.timeout:
        return False
    finally:
        ka.close()


CHUNK_SIZE = 65472
ACK = "3c685354435004000000003e000000003c5354435000003e"


def _build_chunk_ack(chunk_num: int) -> bytes:
    offset = chunk_num * CHUNK_SIZE
    body = struct.pack("<I", offset)
    checksum = sum(body) & 0xFFFF
    hdr = b"<hSTCP" + struct.pack("<I", 4) + b"\x00>"
    ftr = b"<STCP" + struct.pack("<H", checksum) + b">"
    return hdr + body + ftr


def _build_download_cmd(filepath: str) -> bytes:
    body = bytearray(64)
    body[8] = 0x02; body[10] = 0x04; body[24] = 0x01
    path_bytes = filepath.encode("ascii")[:30]
    body[32:32 + len(path_bytes)] = path_bytes
    tag = b"STNC"
    hdr = b"<h" + tag + struct.pack("<I", 64) + b"\x00>"
    ftr = b"<" + tag + struct.pack("<H", sum(body) & 0xFFFF) + b">"
    return hdr + bytes(body) + ftr


def _do_setup(sock):
    for cmd_hex in SETUP_CMDS:
        sock.sendall(bytes.fromhex(cmd_hex))
        _recv(sock, idle=0.1, max_t=3.0)
    sock.sendall(bytes.fromhex(REHANDSHAKE))
    _recv(sock, idle=0.1, max_t=2.0)


def download_session(session_name: str, expected_size: int = 0, ip: str = DEVICE_IP) -> bytes:
    """Download a session file from the device. Returns raw .xrz bytes."""
    filepath = f"1:/mem/{session_name}"
    if not filepath.endswith(".xrz"):
        filepath += ".xrz"

    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(10.0)
    sock.connect((ip, TCP_PORT))
    _do_setup(sock)

    sock.sendall(_build_download_cmd(filepath))
    _recv(sock, idle=0.5, max_t=5.0)

    sock.sendall(bytes.fromhex(ACK))

    all_data = bytearray()
    chunk_num = 1
    while True:
        chunk = _recv(sock, idle=2.0, max_t=30.0)
        if not chunk:
            break
        all_data.extend(chunk)
        if expected_size and len(all_data) >= expected_size + 500:
            break
        if len(chunk) < 30000:
            break
        sock.sendall(_build_chunk_ack(chunk_num))
        chunk_num += 1

    sock.close()

    # Parse STCP frames
    raw = bytes(all_data)
    clean = bytearray()
    pos = 0
    while pos < len(raw):
        if pos + 12 > len(raw) or raw[pos:pos + 6] != b"<hSTCP":
            break
        body_len = struct.unpack_from("<I", raw, pos + 6)[0]
        data_start = pos + 12 + 4
        data_end = pos + 12 + body_len
        clean.extend(raw[data_start:data_end])
        pos = data_end + 8

    return bytes(clean)


def list_sessions(ip: str = DEVICE_IP) -> list[Session]:
    """Connect to device and return list of available sessions."""
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(10.0)
    sock.connect((ip, TCP_PORT))

    all_responses = bytearray()
    for cmd_hex in SETUP_CMDS:
        sock.sendall(bytes.fromhex(cmd_hex))
        resp = _recv(sock, idle=0.1, max_t=3.0)
        all_responses.extend(resp)

    sock.sendall(bytes.fromhex(REHANDSHAKE))
    resp = _recv(sock, idle=1.0, max_t=10.0)
    all_responses.extend(resp)
    sock.close()

    raw = bytes(all_responses)
    sessions = []

    csv_idx = raw.find(b"name,size,date,hour")
    if csv_idx < 0:
        return sessions

    csv_data = raw[csv_idx:].split(b"\x00")[0]
    lines = csv_data.decode("ascii", errors="ignore").strip().split("\r\n")

    for line in lines[1:]:
        parts = line.split(",")
        if len(parts) >= 4 and parts[0].endswith(".xrz"):
            sessions.append(Session(
                name=parts[0],
                size=int(parts[1]) if parts[1].isdigit() else 0,
                date=parts[2],
                time=parts[3],
                laps=parts[4] if len(parts) > 4 else "",
                track=parts[8] if len(parts) > 8 else "",
            ))

    return sessions
