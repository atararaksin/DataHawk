REHANDSHAKE = "3c685354435008000000003e00000000060800003c535443500e003e"


@dataclass
class Session:
    """A single session on the MyChron 5 device."""
    name: str
    size: int
    date: str
    time: str
    laps: str
    track: str


def _recv(sock, idle=0.3, max_t=5.0):
    data = bytearray()
    deadline = time.time() + max_t
    last = time.time()
    while time.time() < deadline:
        wait = min(idle - (time.time() - last), deadline - time.time())
        if wait <= 0:
            break
        sock.settimeout(max(0.05, wait))
        try:
            chunk = sock.recv(65536)
            if not chunk:
                break
            data.extend(chunk)
            last = time.time()
        except socket.timeout:
            if data and (time.time() - last) >= idle:
                break
    return bytes(data)


def check_device(ip: str = DEVICE_IP) -> bool:
    """Return True if device responds to UDP keepalive."""
    ka = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    ka.settimeout(3.0)
    ka.sendto(b"aim-ka", (ip, UDP_PORT))
    try:
        ka.recvfrom(4096)
        return True
    except socket.timeout:
        return False
    finally:
        ka.close()


def list_sessions(ip: str = DEVICE_IP) -> list[Session]:
    """Connect to device and return list of available sessions."""
    sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    sock.settimeout(10.0)
    sock.connect((ip, TCP_PORT))

    all_responses = bytearray()
    for cmd_hex in SETUP_CMDS:
        sock.sendall(bytes.fromhex(cmd_hex))
        resp = _recv(sock, idle=0.1, max_t=3.0)
        all_responses.extend(resp)

    sock.sendall(bytes.fromhex(REHANDSHAKE))
    resp = _recv(sock, idle=1.0, max_t=10.0)
    all_responses.extend(resp)
    sock.close()

    raw = bytes(all_responses)
    sessions = []

    csv_idx = raw.find(b"name,size,date,hour")
    if csv_idx < 0:
        return sessions

    csv_data = raw[csv_idx:].split(b"\x00")[0]
    lines = csv_data.decode("ascii", errors="ignore").strip().split("\r\n")

    for line in lines[1:]:
        parts = line.split(",")
        if len(parts) >= 4 and parts[0].endswith(".xrz"):
            sessions.append(Session(
                name=parts[0],
                size=int(parts[1]) if parts[1].isdigit() else 0,
                date=parts[2],
                time=parts[3],
                laps=parts[4] if len(parts) > 4 else "",
                track=parts[8] if len(parts) > 8 else "",
            ))

    return sessions
